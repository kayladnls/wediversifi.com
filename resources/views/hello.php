                <h2>
                    <a name="user-content-the-spark" class="anchor" href="#the-spark" aria-hidden="true"><span
                            class="octicon octicon-link"></span></a>The spark</h2>

                <p>Kayla (<a href="http://www.twitter.com/kayladnls">@kayladns</a>) and I (<a
                        href="http://www.twitter.com/kayladnls">@gabidavila</a>) were in <a href="http://laracon.eu">LaraconEU</a>
                    2014 talking to <a href="http://www.twitter.com/stauffermatt">Matt Stauffer</a>, and it was a
                    conversation post a <a
                        href="http://www.slideshare.net/gabidavila/coding-like-a-girl?qid=7e7ce085-0fb4-4cfe-97f3-118f6489b311&amp;v=default&amp;b=&amp;from_search=1">diversity
                        presentation</a>, and Matt said that the he was facing some difficulties regarding contacting
                    the
                    minority/diversity groups, because they don't answer to his emails.</p>

                <p>So when he has a job opening, he tries to advertise to those kinds of groups, but as he receives no
                    feedback, it was hard even getting non-white males to submit to his job posting. He asked us if we
                    had
                    any idea on how to help him through our contacts to send to him people we know of, like women,
                    people of
                    color, and from other ethinicity.</p>

                <h2>
                    <a name="user-content-the-idea" class="anchor" href="#the-idea" aria-hidden="true"><span
                            class="octicon octicon-link"></span></a>The idea</h2>

                <p>So this is when this idea come in place. We want to create a portal for companies and potential
                    candidates, where companies commit to try to increase their diversity rate, and there they create a
                    profile with their vision of diversity and with their Linkedin job feeds, or another tool like
                    that.</p>

                <h2>
                    <a name="user-content-why" class="anchor" href="#why" aria-hidden="true"><span
                            class="octicon octicon-link"></span></a>Why?</h2>

                <p>That way we would have a centralized portal with companies, and we all know that Facebook, Google and
                    other big companies have diversity hiring programs, but they don't know if company A or B are
                    diversity
                    friendly.</p>

                <h2>
                    <a name="user-content-how" class="anchor" href="#how" aria-hidden="true"><span
                            class="octicon octicon-link"></span></a>How?</h2>

                <ul class="task-list">
                    <li>Company profiles</li>
                    <li>User profiles</li>
                    <li>Companies sign up and pledge support for diversity.</li>
                    <li>We pull in a feed of job posting for that company.</li>
                    <li>Applicants apply to job posting.</li>
                    <li>If a company hires based on applicants recieved through us, they gain points and are scored.
                    </li>
                    <li>Companies get a badge for their effort</li>
                </ul>
                <h2>
                    <a name="user-content-what-do-we-need" class="anchor" href="#what-do-we-need"
                       aria-hidden="true"><span
                            class="octicon octicon-link"></span></a>What do we need?</h2>

                <p>Help of the community, this is project that will be done in our free time, so we need designers,
                    frontend, backend, basically anyone who can help us!</p></article>
